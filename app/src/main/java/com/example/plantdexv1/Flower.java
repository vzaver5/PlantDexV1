package com.example.plantdexv1;

class Flower {
    String color;
    String conspicuous;

    public Flower(String color, String conspicuous){
        this.color = color;
        this.conspicuous = conspicuous;
    }

    //Getters
    public String getColor() {
        return color;
    }

    public String getConspicuous() {
        return conspicuous;
    }
}
